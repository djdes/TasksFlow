async function main() {
  let upserted = 0;
  for (const art of ARTICLES) {
    await prisma.article.upsert({
      where: { slug: art.slug },
      create: {
        slug: art.slug,
        title: art.title,
        excerpt: art.excerpt,
        body: art.body as unknown as object,
        coverIcon: art.coverIcon,
        tags: art.tags,
        readMinutes: art.readMinutes,
        publishedAt: art.publishedAt,
      },
      update: {
        title: art.title,
        excerpt: art.excerpt,
        body: art.body as unknown as object,
        coverIcon: art.coverIcon,
        tags: art.tags,
        readMinutes: art.readMinutes,
        publishedAt: art.publishedAt,
      },
    });
    upserted += 1;
  }
  console.log(`[seed-articles] upserted=${upserted}`);
}

main()
  .catch((error) => {
    console.error(error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
