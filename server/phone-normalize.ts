/**
 * Нормализация номера телефона перед сравнением и сохранением.
 *
 * ВАЖНО — текущая реализация исторически убирает только пробелы
 * и дефисы. То есть `+7 (495) 123-45-67`, `+7(495)1234567` и
 * `+7.495.123.45.67` будут храниться как РАЗНЫЕ записи: пользователь
 * может зарегистрироваться повторно с тем же фактическим номером.
 *
 * Расширять до strict-normalization (`replace(/\D/g,"")`) безопасно
 * только после миграции существующих строк users.phone, иначе lookup
 * по номеру перестанет находить юзеров, заведённых до миграции. До
 * миграции — не менять поведение этой функции, только централизация.
 */
export function normalizePhone(phone: string): string {
  return phone.replace(/\s+/g, "").replace(/-/g, "");
}
